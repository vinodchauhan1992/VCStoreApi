const mongoose = require("mongoose");
const schema = mongoose.Schema;

const userStatusesSchema = new schema({
  id: {
    type: String,
    required: true,
  },
  status: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  dateAdded: {
    type: Date,
    required: true,
  },
  dateModified: {
    type: Date,
    required: true,
  },
});

module.exports = mongoose.model("user_statuses", userStatusesSchema);
